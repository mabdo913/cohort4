const calculatorFunctions = {

    addNumbers: (num1,num2) => {      
        return num1+num2;
    },

    subtractNumbers: (num1,num2) => {
        return num1-num2;
    },

    multiplyNumbers: (num1,num2) => {
      return num1*num2;
    },
    
    divideNumbers: (num1,num2) => {
      return num1/num2;
    }
}

export default calculatorFunctions;